2014年4月3日-4日，应安全与防护协同创新中心和爆炸科学与技术国家重点实验室的邀请，
国际著名爆轰专家、美国工程院院士、马里兰大学Elaine S. Oran教授来实验室进行学术
交流与访问，在此期间先后做了题为“Turbulence and Energetic Reactive Flows”和
“Effects of Turbulence, Scale on Flame Acceleration, Detonations, and DDT: 
Experiments at Lake Lynn Laboratories”的精彩学术报告。
爆炸科学与技术国家重点实验室主任张庆明教授、副主任王成教授、刘庆明教授、孙远翔
副教授，北京大学、南京理工大学等协同单位，清华大学、中国矿业大学（北京）等师生
聆听了报告会。报告会由王成教授主持。

    Elaine S Oran院士于1972年获得耶鲁大学工程与应用科学系博士学位，长期致力于
    爆轰物理、流体力学和反应流等方向的研究，获得过多项国际荣誉和奖励，包括美国总
    统奖、终身成就奖、阿瑟弗莱明奖、燃烧学会泽尔多维奇金奖等。Elaine S. Oran院士
    同时也是美国物理学会会士、美国航空航天学会会士，并曾经担任多个国际著名期刊主
    编、副主编；还担任密歇根大学兼职教授、利兹大学客座教授以及威尔士大学荣誉教授。

    在报告会上，Elaine S Oran院士结合其团队的研究工作，就大系统燃烧极限、火焰加
    速机理、爆燃到爆轰的突变（DDT）和超新星DDT等问题的研究进展进行了详细的阐述。
    报告引起了师生们的强烈关注和浓厚兴趣，大家就关心的内容进行了热烈的交流和探讨。
    访问期间，机电学院和重点实验室领导陪同Elaine S. Oran院士参观了爆炸科学与技术
    国家重点实验室计算与仿真中心以及中关村校区爆炸安全研究部部分实验室，双方就相
    关学科专业在人才联合培养与科研合作等方面进行了探讨。


